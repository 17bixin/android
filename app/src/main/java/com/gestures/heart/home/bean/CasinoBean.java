package com.gestures.heart.home.bean;

public class CasinoBean {

    private String title;
    private int count;

    public CasinoBean(String title, int count) {
        this.title = title;
        this.count = count;
    }

    public String getTitle() {
        return title;
    }

    public int getCount() {
        return count;
    }
}
